# String Concatenation using Python
  
  
# Defining strings and taking the inputs
str1 = input("Enter the string 1: ")

str2 = input("Enter the string 2: ")
  

# % Operator is used here to combine the string 
print("The final string is:")
print("% s % s" % (str1, str2)) 


